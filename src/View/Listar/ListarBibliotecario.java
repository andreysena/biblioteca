    private void btnAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAlterarActionPerformed
        // TODO add your handling code here:
        if(JTableBibliotecario.getSelectedRow() < 0){
            
            JOptionPane.showMessageDialog(null, "Selecione uma linha da tabela!");
        }else{
            String cod, nome, nasc, telefone, biblio;
            int linha = JTableBibliotecario.getSelectedRow();
            
            cod = (String) JTableBibliotecario.getValueAt(linha, 0);
            nome = (String) JTableBibliotecario.getValueAt(linha, 1);
            nasc = (String) JTableBibliotecario.getValueAt(linha, 2);
            telefone = (String) JTableBibliotecario.getValueAt(linha, 3);
            if(JTableBibliotecario.getValueAt(linha, 5) == null){
                biblio = "0"; 
            }else{
                biblio = (String) JTableBibliotecario.getValueAt(linha, 5);
            }
            
            
            Alt_Bibliotecario alterar = new Alt_Bibliotecario();
            alterar.buscar(cod, nome, nasc, telefone, biblio);
            alterar.setVisible(true);
        }
         
    }//GEN-LAST:event_btnAlterarActionPerformed

