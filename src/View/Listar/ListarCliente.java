    public void CarregarLista(){
        DefaultTableModel modelo = (DefaultTableModel) JTableCliente.getModel();
        
        modelo.setNumRows(0);
        
        DAO.DaoCliente itensCliente = new DaoCliente();
        ResultSet rs = itensCliente.listar();
        
        try{
            while(rs.next()){
                modelo.addRow(new Object[]{
                    rs.getString(1),
                    rs.getString(2),
                    rs.getString(3),
                    rs.getString(4),
                    rs.getString(5),
                });
            }
            

        }catch(SQLException msg){
            throw new RuntimeException(msg);
        }
        
        
    }
    
