    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        if(JTableCliente.getSelectedRow() < 0 ){
            JOptionPane.showMessageDialog(null, "Selecione uma linha da tabela!");
        }else{
            int linha = JTableCliente.getSelectedRow();
            int id = Integer.parseInt((String)JTableCliente.getValueAt(linha, 0));
            
            DaoCliente delete = new DaoCliente();
            delete.excluir(id);
            
            JOptionPane.showMessageDialog(null, "Exclusão realizada com sucesso!");
            
            CarregarLista();
        
        }
    }//GEN-LAST:event_btnExcluirActionPerformed

    private void btnAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAlterarActionPerformed
        // TODO add your handling code here:
        if(JTableCliente.getSelectedRow() < 0){
            JOptionPane.showMessageDialog(null, "Selecione uma linha da tabela!");
        }else{
            
            String cod, nome, cpf, nasc, email;
            int linha = JTableCliente.getSelectedRow();
            
            cod = (String) JTableCliente.getValueAt(linha, 0);
            nome = (String) JTableCliente.getValueAt(linha, 1);
            cpf = (String) JTableCliente.getValueAt(linha, 2);
            nasc = (String) JTableCliente.getValueAt(linha, 3);
            email = (String) JTableCliente.getValueAt(linha, 4);
            
            Alt_Cliente alterar = new Alt_Cliente();
            alterar.buscar(cod, nome, cpf, nasc, email);
            alterar.setVisible(true);
            
        }
        
    }//GEN-LAST:event_btnAlterarActionPerformed

