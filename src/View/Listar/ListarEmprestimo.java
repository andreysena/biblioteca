    private void btnAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAlterarActionPerformed
        // TODO add your handling code here:
        if(JTableEmprestimo.getSelectedRow() < 0){
            JOptionPane.showMessageDialog(null, "Selecione uma linha da tabela!");
        }
        String cod, retirada, devolucao;
        int biblio, cliente;
        int linha = JTableEmprestimo.getSelectedRow();
        
        cod = (String) JTableEmprestimo.getValueAt(linha, 0);
        retirada = (String) JTableEmprestimo.getValueAt(linha, 1);
        devolucao = (String) JTableEmprestimo.getValueAt(linha, 2);
        biblio = Integer.parseInt((String) JTableEmprestimo.getValueAt(linha, 4));
        cliente =  Integer.parseInt((String) JTableEmprestimo.getValueAt(linha, 6));
        
        Alt_Emprestimo alterar = new Alt_Emprestimo();
        alterar.buscar(cod, retirada, devolucao, biblio, cliente);
        alterar.setVisible(true);
    }//GEN-LAST:event_btnAlterarActionPerformed

