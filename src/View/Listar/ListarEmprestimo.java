    public void CarregarLista(){
        DefaultTableModel modelo = (DefaultTableModel) JTableEmprestimo.getModel();        
        modelo.setNumRows(0);
        
        DAO.DaoEmprestimo itensAutor = new DaoEmprestimo();
        ResultSet rs = itensAutor.listar();
        
        try{
            while(rs.next()){
                modelo.addRow(new Object[]{
                   rs.getString(1),
                   rs.getString(2),
                   rs.getString(3),
                   rs.getString(4),
                   rs.getString(5),
                   rs.getString(6),
                   rs.getString(7)
                });
            }
            
        }catch(SQLException msg){
            throw new RuntimeException(msg);
        }
    }
